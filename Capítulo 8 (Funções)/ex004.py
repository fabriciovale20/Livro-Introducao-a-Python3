"""
    Exercício 04

    Escreva uma função que receba a base e a altura de um trângulo e retorne sua área (A = (base x altura) / 2).
Valores esperados:
área_triângulo(6, 9) == 27
área_triângulo(5, 8) == 20
"""

def área_triângulo(base, altura):
    return (base * altura) / 2

print(área_triângulo(6, 9))
print(área_triângulo(5, 8))
